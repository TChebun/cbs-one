let table = document.querySelector('#tbody');

let items = document.querySelectorAll('#pagination li a');

let showPage = (function () {
  let pageActive;

  return function (item) {
    if (pageActive) {
      pageActive.classList.remove('active');
    }

    pageActive = item;

    item.classList.add('active');


    let pageNum = +item.innerHTML;

    let start = (pageNum - 1) * notesOnPage,
      end = start + notesOnPage;
    let notes = transactions.slice(start, end);

    table.innerHTML = '';

    for (let note of notes) {
      let tr = document.createElement('tr');
      table.appendChild(tr);
      tr.classList.add('transaction__body');
      createCell(note.rank, tr, 'Rank');
      createCell(note.date, tr, 'Date');
      createCell(note.name, tr, 'Name');
      createCell(note.balance, tr, 'Balance');
    }
  };
}());

showPage(items[0]);

for (let item of items) {
  item.addEventListener('click', function (event) {
    event.preventDefault();
    showPage(this);
  });
}

function createCell(text, tr, newAttr) {
  let td = document.createElement('td');
  td.classList.add('transaction__body-item');
  td.innerHTML = text;
  tr.appendChild(td);
  td.setAttribute('data-label', newAttr);
}

